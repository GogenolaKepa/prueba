// Ejercicios sobre Variables y Operadores

// a - Sumar dos variables numéricas
document.write("<h3>Variables y Operadores</h3>");
let num1 = 10;
let num2 = 5;
let resultadoSuma = num1 + num2;
console.log("Resultado de la suma:", resultadoSuma);

// b - Concatenar dos variables de tipo string
let str1 = "Hola";
let str2 = " Mundo";
let resultadoConcat = str1 + str2;
console.log("Resultado de la concatenación:", resultadoConcat);

// c - Sumar el largo de dos strings
